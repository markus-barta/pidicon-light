/**
 * Total pixel width of a string (no trailing spacing).
 * @param {string} text
 * @returns {{ width: number, height: number }}
 */
export function measureText(text) {
  if (!text) return { width: 0, height: FONT_SPECS.HEIGHT };
  let w = 0;
  for (const ch of text) {
    w += FONT_SPECS.WIDTH + FONT_SPECS.SPACING;
  }
  if (w > 0) w -= FONT_SPECS.SPACING;
  return { width: w, height: FONT_SPECS.HEIGHT };
}

